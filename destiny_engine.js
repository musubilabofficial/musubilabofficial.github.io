    calculateLPN: (birthday) => {
        const digits = birthday.replace(/\D/g, '');
        let sum = digits.split('').reduce((acc, d) => acc + parseInt(d), 0);
        if (sum === 11 || sum === 22 || sum === 33) return sum;
        while (sum > 9) {
            sum = sum.toString().split('').reduce((acc, d) => acc + parseInt(d), 0);
            if (sum === 11 || sum === 22 || sum === 33) return sum;
        }
        return sum;
    },

