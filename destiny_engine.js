        const date = new Date(birthday);
        const month = date.getMonth() + 1;
        const day = date.getDate();
        if ((month === 3 && day >= 21) || (month === 4 && day <= 19) || (month === 7 && day >= 23) || (month === 8 && day <= 22) || (month === 11 && day >= 22) || (month === 12 && day <= 21)) return 'fire';
